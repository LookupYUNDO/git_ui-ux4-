
let age = prompt(`나이를 입력해주세요`)

if(age >= 20){
    console.log(`${age}세는 성인입니다.`)

}
else if(age >= 17){
    
    console.log(`${age}세는 고등학생입니다.`)

}
else if(age >= 14){
    
    console.log(`${age}세는 중학생입니다.`)

}
else if(age >= 8){
    
    console.log(`${age}세는 초등학생입니다.`)

}

else {
    console.log(`${age}세는 영유아 압니다.`)
}
